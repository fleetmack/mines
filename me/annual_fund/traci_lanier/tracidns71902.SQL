  SELECT ADVANCE.MAILING_LIST.ID_NUMBER,   
         ADVANCE.MAILING_LIST.MAIL_LIST_CODE,   
         ADVANCE.ENTITY.PREF_NAME_SORT,   
         ADVANCE.MAILING_LIST.START_DT,   
         ADVANCE.MAILING_LIST.STOP_DT,   
         ADVANCE.ENTITY.PREF_CLASS_YEAR,   
         ADVANCE.ENTITY.PREF_MAIL_NAME,   
         ADVANCE.A_DW_ADDRESS_GOOD.P_STREET1,   
         ADVANCE.A_DW_ADDRESS_GOOD.P_STREET2,   
         ADVANCE.A_DW_ADDRESS_GOOD.P_FOREIGN_CITYZIP,   
         ADVANCE.A_DW_ADDRESS_GOOD.P_CITYLINE  
    FROM ADVANCE.A_DW_ADDRESS_GOOD,   
         ADVANCE.ENTITY,   
         ADVANCE.MAILING_LIST  
   WHERE ( ADVANCE.A_DW_ADDRESS_GOOD.ID_NUMBER = ADVANCE.ENTITY.ID_NUMBER ) and  
         ( ADVANCE.ENTITY.ID_NUMBER = ADVANCE.MAILING_LIST.ID_NUMBER ) and
			( ADVANCE.MAILING_LIST.MAIL_LIST_CODE = 'DNS' ) AND
			( ADVANCE.ENTITY.RECORD_TYPE_CODE = 'AL') AND
			( ADVANCE.ENTITY.PERSON_OR_ORG = 'P' ) AND
 			( ADVANCE.MAILING_LIST.START_DT > '19990701' )  

